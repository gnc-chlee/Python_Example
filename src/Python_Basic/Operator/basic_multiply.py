# 정수 곱셈
a = 4
b = 3
result1 = a * b
print(result1)  # 출력: 12

# 실수 곱셈
x = 2.5
y = 1.5
result2 = x * y
print(result2)  # 출력: 3.75

# 문자열 반복
word = "Python"
result = word * 3
print(result)  # 출력: PythonPythonPython

# 문자열과 숫자의 곱셈
message = "Drone, "
repeat = 3
result = message * repeat
print(result)  # 출력: Drone, Drone, Drone,
